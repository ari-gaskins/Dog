package Dog;

public class Dog {
    public String name;

    public void bark() {
        System.out.println(name + " says Ruff!");
    }

    public void chaseCat() {
        System.out.println(name + " is chasing a cat!");
    }
    
    public void eat() {
        System.out.println(name + " is eating kibble!");
    }

}
